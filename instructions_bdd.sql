Pour créer la base de donnée en local :

-- 1. Lancer le serveur sur XAMP/WAMP/MAMP 
-- 2. Taper l'URL suivant : http://localhost/phpmyadmin
-- 3. Créer une nouvelle base de donnée et lui donner le nom "discord_bdd"
-- 4. Copier coller l'entièreté du fichier discord_bdd.sql dans l'onglet 'SQL' dans la base de données sur phpmyadmin puis exécuter. 

